func TestInsertionSort_StringArrays(t *testing.T) {
	t.Parallel()

	type testCase struct {
		name     string
		array    []string
		expected []string
	}

	tests := []testCase{
		{
			name:     "sort nil array",
			array:    nil,
			expected: nil,
		},
		{
			name:     "sort empty array",
			array:    []string{},
			expected: []string{},
		},
		{
			name:     "sort array with one element",
			array:    []string{"foobar"},
			expected: []string{"foobar"},
		},
		{
			name:     "sort array with two elements",
			array:    []string{"bot", "arm"},
			expected: []string{"arm", "bot"},
		},
		{
			name:     "sort array with three elements",
			array:    []string{"bot", "car", "arm"},
			expected: []string{"arm", "bot", "car"},
		},
		{
			name: "sort array sample 1",
			array: []string{
				"d", "c", "z", "house", "bananas", "love", "cook", "kill", "satan", "computer", "house", "a",
				"lovers", "z", "x", "job",
			},
			expected: []string{"a", "bananas", "c", "computer", "cook", "d", "house", "house", "job", "kill", "love",
				"lovers", "satan", "x", "z", "z",
			},
		},
		{
			name: "sort array where all elements are equal",
			array: []string{
				"bananas", "bananas", "bananas", "bananas", "bananas", "bananas", "bananas", "bananas", "bananas",
				"bananas", "bananas",
			},
			expected: []string{
				"bananas", "bananas", "bananas", "bananas", "bananas", "bananas", "bananas", "bananas", "bananas",
				"bananas", "bananas",
			},
		},
		{
			name: "sort array already sorted",
			array: []string{
				"a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k", "l", "m", "n", "o", "p", "q", "r", "s", "t", "u",
				"v", "w", "x", "y", "z",
			},
			expected: []string{
				"a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k", "l", "m", "n", "o", "p", "q", "r", "s", "t", "u",
				"v", "w", "x", "y", "z",
			},
		},
		{
			name: "sort array reversely sorted",
			array: []string{
				"z", "y", "x", "w", "v", "u", "t", "s", "r", "q", "p", "o", "n", "m", "l", "k", "j", "i", "h", "g", "f",
				"e", "d", "c", "b", "a",
			},
			expected: []string{
				"a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k", "l", "m", "n", "o", "p", "q", "r", "s", "t", "u",
				"v", "w", "x", "y", "z",
			},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			InsertionSort[string](tt.array, strings.Compare)
			assert.Equal(t, tt.expected, tt.array)
		})
	}
}
